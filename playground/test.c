int main(void)
{
	int row = 10;
	int col = 5;
	int i = 0;
	int j = 0;

	while (i < row)
	{
		j = 0;
		while (j < col)
		{
			printf("%d", i);
			j++;
		}
		i++;
		printf("\n");
	}


	return (0);
}
